package hainiu.H0401;
//字符串和数字的互换
public class H7 {
    public static void main(String[] args) {
        Integer num1=233;
        Integer num2 = new Integer("114514");
        System.out.println(num1);
        System.out.println(num2);
        num1+=1;
        num2+=1;
        System.out.println(num1);
        System.out.println(num2);
        int x=Integer.parseInt("23333");
        String y=String.valueOf(1415926);
        System.out.println(x);
        System.out.println(y);
    }

}
